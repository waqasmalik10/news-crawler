export const getSimilarityScore = (targetNews, news) => {
    const targetKeywords = getNewsKeyWords(targetNews);
    const newsKeywords = getNewsKeyWords(news);

    let score=0;

    for(const targetKeyword of targetKeywords){
        for(const newsKeyword of newsKeywords){
            if(targetKeyword === newsKeyword){
                score++;
            }
        }
    }
    if(score>0){
        const avg = (targetKeywords.length + newsKeywords.length)/2;	
        score = score/avg;

    }
    return score;
}


const getNewsKeyWords = (news) => {

    const splittedNews = news.split(" ");
    const noisyWords = getNoisyWords();
    
    const keyWords = []

    for(let i=0; i<splittedNews.length; i++ ){

        if(noisyWords.includes(splittedNews[i])){
            continue;
        }

        if(!keyWords.includes(splittedNews[i])){
            keyWords.push(splittedNews[i]);
        }
        
    }
    return keyWords;
}